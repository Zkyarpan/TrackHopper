function Button({
  className,
  variant = "default",
  size = "default",
  render,
  nativeButton,
  ...props
}: ButtonPrimitive.Props & VariantProps<typeof buttonVariants>) {
  // When a render prop is provided the underlying element may not be a native
  // <button> (e.g. Next.js <Link> renders an <a>). Suppress the Base UI
  // accessibility warning by defaulting nativeButton to false in that case.
  const resolvedNativeButton = nativeButton ?? (render ? false : undefined);

  return (
    <ButtonPrimitive
      data-slot="button"
      className={cn(buttonVariants({ variant, size, className }))}
      render={render}
      nativeButton={resolvedNativeButton}
      {...props}
    />
  )
}

export { Button, buttonVariants }
